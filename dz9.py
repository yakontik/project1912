def make_shirt(size, message):
    print(f"The size of the shirt is {size}.")
    print(f"The message on the shirt is '{message}'.")
make_shirt("S", "I love Step")
make_shirt("L", "I love Ukraine")
make_shirt("M", "I love Rivne")


